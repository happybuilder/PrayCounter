public class PrayItem {

    private String prayName;
    private String roundSize;
    private String prayDate;
    public String getPrayName() {
        return prayName;
    }

    public void setPrayName(String prayName) {
        this.prayName = prayName;
    }

    public String getRoundSize() {
        return roundSize;
    }

    public void setRoundSize(String roundSize) {
        this.roundSize = roundSize;
    }

    public String getPrayDate() {
        return prayDate;
    }

    public void setPrayDate(String prayDate) {
        this.prayDate = prayDate;
    }

}
